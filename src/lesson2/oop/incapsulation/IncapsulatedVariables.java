package lesson2.oop.incapsulation;

public class IncapsulatedVariables {
    private char myChar = 'c';
    private int a =1;

    public char getMyChar() {
        return myChar;
    }

    public void setMyChar(char myChar) {
        this.myChar = myChar;
    }

    public int getA() {
        return a;
    }

    public void setA(int a) {
        this.a = a;
    }
}
